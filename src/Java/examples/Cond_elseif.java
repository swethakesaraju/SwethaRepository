package Java.examples;

public class Cond_elseif {
	public static void main(String args[]) {
		int a=30;
		if(a==20) {
			System.out.println("a value is 20");
		}
		else if(a==50) {
			System.out.println("a value is 50");
		}
			else if(a==40) {
				System.out.println("a value is 40");
			}
			else{
				System.out.println("else stmt executed");
			}
		
	}

}
